def ensure_playwright_runtime(auto_install: bool = True) -> dict[str, Any]:
    """Validate/install the local generated-playwright runtime before execution."""
    started = time.time()
    log_event('playwright_execution', 'Runtime preflight started: checking npm/npx and local Playwright dependencies', progress=12, details={'auto_install': auto_install})
    status: dict[str, Any] = {
        'ok': False,
        'stage': 'playwright_runtime_preflight',
        'generated_playwright_dir': str(GENERATED_PLAYWRIGHT_DIR),
        'auto_install': auto_install,
        'npm_available': bool(resolve_command('npm')),
        'npx_available': bool(resolve_command('npx')),
        'installed_before': _playwright_bin_exists(),
        'steps': [],
    }
    if not status['npm_available']:
        status['error'] = 'npm not found. Install Node.js LTS with npm and reopen the terminal/GUI.'
        log_event('playwright_execution', status['error'], status='error', progress=13)
        _write_fallback_playwright_html('Playwright runtime preflight failed', status)
        return status
    if not status['npx_available']:
        status['error'] = 'npx not found. Install Node.js LTS with npm/npx and reopen the terminal/GUI.'
        log_event('playwright_execution', status['error'], status='error', progress=13)
        _write_fallback_playwright_html('Playwright runtime preflight failed', status)
        return status
    if not (GENERATED_PLAYWRIGHT_DIR / 'package.json').exists():
        status['error'] = 'generated-playwright/package.json not found. The Playwright framework folder is incomplete.'
        log_event('playwright_execution', status['error'], status='error', progress=13)
        _write_fallback_playwright_html('Playwright runtime preflight failed', status)
        return status

    if not _playwright_bin_exists():
        if not auto_install:
            status['error'] = 'Playwright npm dependencies are missing. Run npm install inside generated-playwright.'
            log_event('playwright_execution', status['error'], status='error', progress=14)
            _write_fallback_playwright_html('Playwright runtime preflight failed', status)
            return status
        log_event('playwright_execution', 'Playwright npm dependencies are missing. Running npm install inside generated-playwright.', progress=15)
        install = run_command(['npm', 'install', '--registry=https://registry.npmjs.org/'], cwd=GENERATED_PLAYWRIGHT_DIR, timeout=1200)
        status['steps'].append({'name': 'npm_install', 'ok': install.ok, 'returncode': install.returncode, 'command': install.command, 'stdout': install.stdout[-4000:], 'stderr': install.stderr[-4000:], 'error': install.error})
        if not install.ok:
            status['error'] = 'npm install failed. Playwright cannot execute until generated-playwright dependencies are installed.'
            log_event('playwright_execution', status['error'], status='error', progress=16, details={'stderr_tail': install.stderr[-1000:]})
            _write_fallback_playwright_html('Playwright runtime preflight failed', status)
            return status
        log_event('playwright_execution', 'npm install completed successfully for generated-playwright.', status='done', progress=22)

    version = run_command(['npx', '--no-install', 'playwright', '--version'], cwd=GENERATED_PLAYWRIGHT_DIR, timeout=60)
    status['steps'].append({'name': 'playwright_cli_version', 'ok': version.ok, 'returncode': version.returncode, 'command': version.command, 'stdout': version.stdout[-1000:], 'stderr': version.stderr[-1000:], 'error': version.error})
    if not version.ok:
        status['error'] = 'Playwright CLI is still unavailable after dependency check. Run npm install in generated-playwright and retry.'
        log_event('playwright_execution', status['error'], status='error', progress=24, details={'stderr_tail': version.stderr[-1000:]})
        _write_fallback_playwright_html('Playwright runtime preflight failed', status)
        return status
    log_event('playwright_execution', f"Playwright CLI available: {(version.stdout or version.stderr).strip()}", status='done', progress=25)

    # Browser install is idempotent and normally quick when Chromium already exists.
    log_event('playwright_execution', 'Ensuring Chromium browser is installed for Playwright.', progress=27)
    browser = run_command(['npx', '--no-install', 'playwright', 'install', 'chromium'], cwd=GENERATED_PLAYWRIGHT_DIR, timeout=900)
    status['steps'].append({'name': 'playwright_install_chromium', 'ok': browser.ok, 'returncode': browser.returncode, 'command': browser.command, 'stdout': browser.stdout[-3000:], 'stderr': browser.stderr[-3000:], 'error': browser.error})
    status['chromium_install_ok_or_already_present'] = browser.ok
    log_event('playwright_execution', 'Chromium browser check completed.' if browser.ok else 'Chromium install command returned a warning; execution may still work if browser is already cached.', status='done' if browser.ok else 'warning', progress=30, details={'ok': browser.ok, 'stderr_tail': browser.stderr[-800:]})

    status['ok'] = True
    status['installed_after'] = _playwright_bin_exists()
    status['duration_seconds'] = round(time.time() - started, 2)
    return status



def _is_pipeline_fallback_report(index: Path) -> bool:
    """Detect fallback reports so stale fallbacks do not mask native Playwright reports."""
    try:
        if not index.exists():
            return False
        text = index.read_text(encoding='utf-8', errors='ignore')[:4000]
        return 'Playwright did not generate a native HTML report' in text or 'Fallback execution report' in text
    except Exception:
        return False


def _reset_playwright_html_report_dirs() -> dict[str, Any]:
    """Remove stale HTML report folders before a new execution.

    This is critical because an older fallback report at reports/html/index.html can
    otherwise make the GUI think the latest native Playwright report is ready, or can
    hide a new native report generated in playwright-report/.
    """
    removed: list[str] = []
    errors: list[str] = []
    dirs = [
        REPORTS_DIR / 'html',
        GENERATED_PLAYWRIGHT_DIR / 'playwright-report',
        GENERATED_PLAYWRIGHT_DIR / 'reports' / 'playwright-report',
        REPORTS_DIR / 'json-shards',
    ]
    for d in dirs:
        try:
            if d.exists():
                shutil.rmtree(d, ignore_errors=True)
                removed.append(str(d.relative_to(GENERATED_PLAYWRIGHT_DIR)))
        except Exception as exc:  # pragma: no cover - environment/filesystem specific
            errors.append(f'{d}: {type(exc).__name__}: {exc}')
    for f in [REPORTS_DIR / 'results.json']:
        try:
            if f.exists():
                f.unlink()
                removed.append(str(f.relative_to(GENERATED_PLAYWRIGHT_DIR)))
        except Exception as exc:
            errors.append(f'{f}: {type(exc).__name__}: {exc}')
    return {'removed': removed, 'errors': errors}


def _copy_default_html_report_if_present() -> dict[str, Any]:
    """Normalize the latest native Playwright HTML report to reports/html.

    The GUI always opens /artifacts/reports/html/index.html. This helper now
    avoids treating an old fallback page as a successful native Playwright report.
    """
    target_dir = REPORTS_DIR / 'html'
    target_index = target_dir / 'index.html'
    candidates = [
        GENERATED_PLAYWRIGHT_DIR / 'playwright-report' / 'index.html',
        GENERATED_PLAYWRIGHT_DIR / 'reports' / 'playwright-report' / 'index.html',
        target_index,
    ]
    inspected: list[str] = []
    for index in candidates:
        try:
            inspected.append(str(index.relative_to(GENERATED_PLAYWRIGHT_DIR)))
            if not index.exists():
                continue
            if _is_pipeline_fallback_report(index):
                # A fallback report is useful only as a last resort; it is not a native Playwright report.
                continue
            source_dir = index.parent
            if source_dir.resolve() == target_dir.resolve():
                return {
                    'ok': True,
                    'native': True,
                    'source': str(source_dir.relative_to(GENERATED_PLAYWRIGHT_DIR)),
                    'target': 'reports/html',
                    'copied': False,
                    'inspected': inspected,
                }
            if target_dir.exists():
                shutil.rmtree(target_dir, ignore_errors=True)
            shutil.copytree(source_dir, target_dir)
            return {
                'ok': True,
                'native': True,
                'source': str(source_dir.relative_to(GENERATED_PLAYWRIGHT_DIR)),
                'target': 'reports/html',
                'copied': True,
                'inspected': inspected,
            }
        except Exception as exc:  # pragma: no cover - Windows/filesystem specific
            return {'ok': False, 'native': False, 'error': f'{type(exc).__name__}: {exc}', 'target': 'reports/html', 'inspected': inspected}
    return {'ok': False, 'native': False, 'reason': 'No native Playwright HTML report index.html found.', 'target': 'reports/html', 'inspected': inspected}


def _run_playwright_streaming(
    args: list[str],
    env: dict[str, str],
    label: str,
    progress_start: int,
    progress_end: int,
    timeout_seconds: int | None = None,
) -> dict[str, Any]:
    """Run a Playwright command with plain-English runtime logs and bounded finalization."""
    resolved = resolve_command(args[0])
    command_display = ' '.join(args)
    stdout_lines: list[str] = []
    stderr_text = ''
    started = time.time()
    if not resolved:
        msg = f'{args[0]} not found; cannot launch Playwright.'
        log_event('playwright_execution', f'{label} failed before launch: {msg}', status='error', progress=progress_start, details={'command': command_display})
        return {'ok': False, 'returncode': None, 'command': command_display, 'cwd': str(GENERATED_PLAYWRIGHT_DIR), 'stdout': '', 'stderr': '', 'error': msg, 'duration_seconds': 0}

    final_args = [resolved, *args[1:]]
    log_event('playwright_execution', f'{label}: Playwright process started. Browser will launch in this step.', progress=progress_start, details={'command': command_display})
    try:
        proc = subprocess.Popen(
            final_args,
            cwd=str(GENERATED_PLAYWRIGHT_DIR),
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            encoding='utf-8',
            errors='replace',
            env=env,
            bufsize=1,
        )
        assert proc.stdout is not None
        last_log = 0.0
        deadline = time.time() + timeout_seconds if timeout_seconds else None
        for line in proc.stdout:
            line = line.rstrip('\n')
            if not line:
                if deadline and time.time() > deadline:
                    proc.kill()
                    stderr_text = f'{label} exceeded timeout of {timeout_seconds} seconds and was stopped.'
                    break
                continue
            stdout_lines.append(line)
            now = time.time()
            low = line.lower()
            if any(token in low for token in ['running ', 'passed', 'failed', 'timed out', 'error', 'retry', 'locator', 'expect', 'report', 'browser', 'worker']) or now - last_log > 8:
                last_log = now
                log_event('playwright_execution', f'{label}: {line[:500]}', progress=min(progress_end, progress_start + 4), details={'command_label': label})
            if deadline and now > deadline:
                proc.kill()
                stderr_text = f'{label} exceeded timeout of {timeout_seconds} seconds and was stopped.'
                break
        try:
            returncode = proc.wait(timeout=15)
        except subprocess.TimeoutExpired:
            proc.kill()
            returncode = proc.wait(timeout=10)
            stderr_text = stderr_text or f'{label} process exceeded timeout while closing and was killed.'
        duration = round(time.time() - started, 2)
        ok = returncode == 0
        log_event('playwright_execution', f"{label} {'passed' if ok else 'completed with issue'} in {duration}s.", status='done' if ok else 'warning', progress=progress_end, details={'returncode': returncode, 'duration_seconds': duration})
        return {
            'ok': ok,
            'returncode': returncode,
            'command': command_display,
            'cwd': str(GENERATED_PLAYWRIGHT_DIR),
            'stdout': '\n'.join(stdout_lines)[-20000:],
            'stderr': stderr_text[-8000:],
            'error': None,
            'duration_seconds': duration,
        }
    except Exception as exc:  # pragma: no cover - environment specific
        duration = round(time.time() - started, 2)
        log_event('playwright_execution', f'{label} crashed before completion: {type(exc).__name__}: {exc}', status='error', progress=progress_end, details={'duration_seconds': duration})
        return {
            'ok': False,
            'returncode': None,
            'command': command_display,
            'cwd': str(GENERATED_PLAYWRIGHT_DIR),
            'stdout': '\n'.join(stdout_lines)[-20000:],
            'stderr': stderr_text[-8000:],
            'error': f'{type(exc).__name__}: {exc}',
            'duration_seconds': duration,
        }


