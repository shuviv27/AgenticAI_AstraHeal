def _normalise_spec_path(path_value: str) -> str:
    value = str(path_value or '').replace('\\', '/').strip()
    if not value:
        return ''
    if 'tests/generated/' in value:
        return value[value.index('tests/generated/'):]
    if value.endswith('.spec.ts') and '/' not in value:
        return f'tests/generated/{value}'
    return value


def _spec_to_feature(spec_path: str) -> str:
    name = Path(_normalise_spec_path(spec_path)).name
    if name.endswith('.spec.ts'):
        return name[:-8]
    return Path(name).stem


def _read_playwright_results_json() -> dict[str, Any]:
    """Read Playwright JSON results from sequential or distributed runs.

    Sequential execution writes reports/results.json. Distributed execution writes one
    JSON result per shard under reports/json-shards/. Aggregating shard JSON prevents
    the RCA engine from treating every selected spec as failed when only one shard/spec
    actually failed.
    """
    single_candidates = [
        REPORTS_DIR / 'results.json',
        GENERATED_PLAYWRIGHT_DIR / 'reports' / 'results.json',
        GENERATED_PLAYWRIGHT_DIR / 'test-results' / 'results.json',
    ]
    for c in single_candidates:
        if c.exists():
            try:
                return {'ok': True, 'path': str(c.relative_to(REPO_ROOT)), 'data': json.loads(c.read_text(encoding='utf-8', errors='replace')), 'mode': 'single'}
            except Exception as exc:
                return {'ok': False, 'path': str(c), 'error': f'{type(exc).__name__}: {exc}', 'data': {}}

    shard_dir = REPORTS_DIR / 'json-shards'
    shard_files = sorted(shard_dir.glob('*.json')) if shard_dir.exists() else []
    if shard_files:
        merged = {'config': {}, 'suites': [], 'errors': [], 'stats': {}}
        parsed_files: list[str] = []
        errors: list[str] = []
        for f in shard_files:
            try:
                data = json.loads(f.read_text(encoding='utf-8', errors='replace'))
                parsed_files.append(str(f.relative_to(REPO_ROOT)))
                if not merged.get('config') and data.get('config'):
                    merged['config'] = data.get('config')
                merged['suites'].extend(data.get('suites') or [])
                merged['errors'].extend(data.get('errors') or [])
            except Exception as exc:
                errors.append(f'{f}: {type(exc).__name__}: {exc}')
        return {'ok': bool(parsed_files), 'path': str(shard_dir.relative_to(REPO_ROOT)), 'files': parsed_files, 'errors': errors, 'data': merged, 'mode': 'shards'}
    return {'ok': False, 'error': 'No Playwright JSON results file found.', 'data': {}}


def _extract_failed_from_playwright_json(data: dict[str, Any]) -> dict[str, Any]:
    failed_tests: list[dict[str, Any]] = []
    all_specs: set[str] = set()
    spec_statuses: dict[str, dict[str, Any]] = {}

    def final_status_for(statuses: list[str], failed: bool) -> str:
        unique = [s for s in dict.fromkeys([str(x).lower() for x in statuses if x]) if s]
        if failed:
            return 'failed'
        if any(s in {'passed', 'expected'} for s in unique):
            return 'passed'
        if any(s == 'skipped' for s in unique):
            return 'skipped'
        return unique[-1] if unique else 'unknown'

    def visit_suite(suite: dict[str, Any], inherited_file: str = '') -> None:
        suite_file = suite.get('file') or inherited_file
        for spec in suite.get('specs') or []:
            spec_file = _normalise_spec_path(spec.get('file') or suite_file or '')
            if spec_file:
                all_specs.add(spec_file)
            title = spec.get('title') or ''
            failed = False
            statuses: list[str] = []
            for test in spec.get('tests') or []:
                status = str(test.get('status') or '').lower()
                if status:
                    statuses.append(status)
                if status and status not in {'expected', 'passed', 'skipped'}:
                    failed = True
                for result in test.get('results') or []:
                    rstatus = str(result.get('status') or '').lower()
                    if rstatus:
                        statuses.append(rstatus)
                    if rstatus in {'failed', 'timedout', 'timed out', 'interrupted'}:
                        failed = True
            if spec_file:
                current = spec_statuses.get(spec_file, {'spec': spec_file, 'feature': _spec_to_feature(spec_file), 'titles': [], 'statuses': [], 'status': 'unknown'})
                current['titles'].append(title)
                current['statuses'].extend(statuses)
                # If any spec in the file failed, keep file-level status failed.
                current_failed = failed or current.get('status') == 'failed'
                current['status'] = final_status_for(current['statuses'], current_failed)
                spec_statuses[spec_file] = current
            if failed:
                failed_tests.append({
                    'spec': spec_file,
                    'feature': _spec_to_feature(spec_file),
                    'title': title,
                    'statuses': list(dict.fromkeys(statuses)),
                })
        for child in suite.get('suites') or []:
            visit_suite(child, suite_file)

    for suite in data.get('suites') or []:
        visit_suite(suite)

    failed_specs = sorted({t['spec'] for t in failed_tests if t.get('spec')})
    passed_specs = sorted([s for s, row in spec_statuses.items() if row.get('status') == 'passed' and s not in failed_specs])
    return {
        'ok': True,
        'all_specs': sorted(all_specs),
        'spec_statuses': spec_statuses,
        'passed_specs': passed_specs,
        'failed_specs': failed_specs,
        'failed_features': [_spec_to_feature(s) for s in failed_specs],
        'failed_tests': failed_tests,
        'failed_count': len(failed_tests),
    }


def _extract_failed_specs_from_stdout(stdout: str, target_args: list[str]) -> list[str]:
    text = str(stdout or '')
    found = set(_normalise_spec_path(m.group(1)) for m in re.finditer(r'(tests[\\/]generated[\\/][\w.\-]+\.spec\.ts)', text))
    return sorted([f for f in found if f]) or list(target_args)


def _write_failed_test_inventory(*, execution_report: dict[str, Any], target_args: list[str], execution_mode: str, source: str = 'latest_execution') -> dict[str, Any]:
    """Persist failed specs so RCA/self-healing can rerun only the affected scripts.

    Playwright's native HTML report is still the primary human report. This JSON file is the
    automation hand-off between execution -> RCA -> patch -> failed-only rerun.
    """
    results_json = _read_playwright_results_json()
    parsed = _extract_failed_from_playwright_json(results_json.get('data') or {}) if results_json.get('data') else {'failed_specs': [], 'failed_features': [], 'failed_tests': [], 'all_specs': [], 'passed_specs': [], 'spec_statuses': {}}
    failed_specs = parsed.get('failed_specs') or []
    stdout_chunks: list[str] = []
    if execution_report.get('result'):
        stdout_chunks.append(str(execution_report.get('result', {}).get('stdout', '')))
    for r in execution_report.get('results') or []:
        if not r.get('ok'):
            stdout_chunks.append(str(r.get('stdout', '')) + '\n' + str(r.get('stderr', '')))
    extraction_note = ''
    if not failed_specs and not execution_report.get('ok'):
        # Try stdout extraction, but never mark every selected target as failed just because
        # Playwright JSON was unavailable. That caused RCA to analyze and rerun already-passed scripts.
        extracted = _extract_failed_specs_from_stdout('\n'.join(stdout_chunks), [])
        failed_specs = extracted
        if not failed_specs:
            extraction_note = 'Playwright execution failed but no exact failed spec could be extracted. RCA is blocked until a JSON report or exact failed spec is available; the pipeline will not assume all selected specs failed.'
    failed_specs = sorted(dict.fromkeys(_normalise_spec_path(s) for s in failed_specs if _normalise_spec_path(s)))
    inventory = {
        'ok': True,
        'source': source,
        'execution_mode': execution_mode,
        'generated_at_epoch_ms': int(time.time() * 1000),
        'target_args': target_args,
        'all_specs': parsed.get('all_specs') or target_args,
        'passed_specs': parsed.get('passed_specs') or [s for s in (parsed.get('all_specs') or target_args) if _normalise_spec_path(s) not in failed_specs],
        'spec_statuses': parsed.get('spec_statuses') or {},
        'failed_specs': failed_specs,
        'failed_features': [_spec_to_feature(s) for s in failed_specs],
        'failed_tests': parsed.get('failed_tests') or [],
        'failed_count': len(failed_specs),
        'results_json': {k: v for k, v in results_json.items() if k != 'data'},
        'native_html_report': 'generated-playwright/reports/html/index.html',
        'note': 'RCA/self-healing rerun uses failed_specs only. The pipeline never assumes all selected specs failed when JSON parsing is unavailable.',
        'extraction_note': extraction_note,
    }
    FAILED_TESTS_INVENTORY.parent.mkdir(parents=True, exist_ok=True)
    FAILED_TESTS_INVENTORY.write_text(json.dumps(inventory, indent=2, ensure_ascii=False) + '\n', encoding='utf-8')
    if failed_specs:
        log_event('playwright_execution', f'Failed-only rerun inventory recorded: {len(failed_specs)} failed spec(s) will be targeted after RCA/self-healing.', status='warning', progress=100, details={'failed_specs': failed_specs})
    elif not execution_report.get('ok'):
        log_event('playwright_execution', 'Execution failed, but no exact failed spec inventory could be built. RCA will request a rerun with JSON reporting instead of analyzing all scripts.', status='error', progress=100, details={'target_args': target_args, 'results_json': inventory.get('results_json'), 'extraction_note': extraction_note})
    else:
        log_event('playwright_execution', 'Failed-only rerun inventory recorded: no failed specs detected.', status='done', progress=100, details={'target_args': target_args})
    return inventory


def read_failed_test_inventory() -> dict[str, Any]:
    if not FAILED_TESTS_INVENTORY.exists():
        return {'ok': False, 'error': 'No failed-test inventory found. Run a generated Playwright execution first.'}
    try:
        data = json.loads(FAILED_TESTS_INVENTORY.read_text(encoding='utf-8', errors='replace'))
        data['ok'] = True
        return data
    except Exception as exc:
        return {'ok': False, 'error': f'{type(exc).__name__}: {exc}', 'path': str(FAILED_TESTS_INVENTORY)}


def _archive_current_html_report(label: str) -> dict[str, Any]:
    src = REPORTS_DIR / 'html'
    if not (src / 'index.html').exists():
        return {'ok': False, 'reason': 'No current native/fallback Playwright HTML report found to archive.'}
    dest = REPORTS_DIR / label
    try:
        if dest.exists():
            shutil.rmtree(dest, ignore_errors=True)
        shutil.copytree(src, dest)
        return {'ok': True, 'source': str(src.relative_to(REPO_ROOT)), 'target': str(dest.relative_to(REPO_ROOT)), 'url': f'/artifacts/reports/{label}/index.html'}
    except Exception as exc:
        return {'ok': False, 'error': f'{type(exc).__name__}: {exc}'}


def _html_escape(value: Any) -> str:
    return str(value if value is not None else '').replace('&', '&amp;').replace('<', '&lt;').replace('>', '&gt;')


def _write_failed_only_consolidated_report(payload: dict[str, Any]) -> Path:
    """Write a complete RCA rerun report that preserves original passed results.

    Native Playwright HTML can only represent the latest execution scope. After a failed-only rerun,
    that native report correctly shows only the rerun specs. This consolidated report provides the
    enterprise-level full picture: originally passed specs remain passed, failed specs are updated
    with the failed-only rerun result, and links to both native reports are retained.
    """
    full_url = payload.get('archived_full_report', {}).get('url') or ''
    rerun_url = '/artifacts/reports/html/index.html'
    original = payload.get('failed_inventory') or {}
    rerun_inventory = payload.get('rerun_failed_inventory') or {}
    original_all = [_normalise_spec_path(s) for s in (original.get('all_specs') or original.get('target_args') or []) if _normalise_spec_path(s)]
    original_failed = set(_normalise_spec_path(s) for s in (original.get('failed_specs') or []) if _normalise_spec_path(s))
    rerun_failed = set(_normalise_spec_path(s) for s in (rerun_inventory.get('failed_specs') or []) if _normalise_spec_path(s))
    rerun_all = set(_normalise_spec_path(s) for s in (rerun_inventory.get('all_specs') or original.get('failed_specs') or []) if _normalise_spec_path(s))
    all_specs = list(dict.fromkeys(original_all + sorted(original_failed)))
    rows = []
    passed_after_patch = 0
    still_failed = 0
    originally_passed = 0
    for idx, spec in enumerate(all_specs, 1):
        feature = _spec_to_feature(spec)
        if spec in original_failed:
            if spec in rerun_failed:
                status = 'Still failing after RCA patch'
                cls = 'bad'
                still_failed += 1
