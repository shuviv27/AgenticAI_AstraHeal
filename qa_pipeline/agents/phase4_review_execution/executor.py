def execute_feature(feature: str = 'login', project: str = 'chromium', use_mcp: bool = True, headed: bool = False, base_url: str = '') -> dict:
    log_event('playwright_execution', f'Starting single-spec Playwright execution for {feature}', progress=15, feature=feature, details={'headed': headed, 'project': project})
    result = run_playwright_test(feature=feature, project=project, use_mcp_context=use_mcp, headed=headed, base_url=base_url)
    log_event('playwright_execution', f"Single-spec Playwright execution {'passed' if result.get('ok') else 'completed with issue'} for {feature}", status='done' if result.get('ok') else 'warning', progress=100, feature=feature, details={'ok': result.get('ok')})
    return result


def _feature_spec_arg(feature: str) -> str:
    feature = (feature or '').strip().lower()
    if feature in {'all', '*', 'suite'}:
        return 'tests/generated'
    return f'tests/generated/{feature}.spec.ts'


def _target_args(feature: str = 'all', features: list[str] | None = None) -> list[str]:
    clean_features = [str(f).strip().lower() for f in (features or []) if str(f).strip()]
    if clean_features:
        return [f'tests/generated/{f}.spec.ts' for f in clean_features]
    return [_feature_spec_arg(feature)]


def _playwright_bin_exists() -> bool:
    if (GENERATED_PLAYWRIGHT_DIR / 'node_modules' / '@playwright' / 'test').exists():
        return True
    if (GENERATED_PLAYWRIGHT_DIR / 'node_modules' / '.bin' / 'playwright').exists():
        return True
    if (GENERATED_PLAYWRIGHT_DIR / 'node_modules' / '.bin' / 'playwright.cmd').exists():
        return True
    return False


